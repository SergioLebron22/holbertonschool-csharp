using System;

namespace MyMath {
    /// <summary>
    /// Operations class
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Method for adding two integer values
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns>the sum of two integers</returns>
        public static int Add(int a, int b) {
            return a + b;
        }
    }

}

